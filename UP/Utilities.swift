import Foundation
import SpriteKit

extension SKScene {
    var screenWidth:CGFloat{
        return UIScreen.mainScreen().bounds.size.width
    }
    var screenHeight:CGFloat{
        return UIScreen.mainScreen().bounds.size.height
    }
}